def area(a, b): 
    """ 
    area(a, b)
    
    Принимает значения a, b и возвращает значение площади
    
    Пример: area(5,10) = 50
    """
    return a * b 

def perimeter(a, b): 
    """ 
    perimeter(a, b)
    
    Принимает значения a, b и возвращает значение периметра

    Пример: perimeter(5, 10) = 30
    """
    return 2*(a + b)