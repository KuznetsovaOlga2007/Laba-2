
def area(a, b):
    '''
    Функция принимает на вход числа a, b - стороны прямоугольника и возвращает площадь Прямоугольника

    Вход: 3 4 Выход: 12
    ''' 
    return a * b 

def perimeter(a, b):
    '''
    Функция принимает на вход числа a, b - стороны прямоугольника и возвращает периметр Прямоугольника
    
    Вход: 2 5 Выход: 14
    ''' 
    return (a + b) * 2 