def area(a, b):
    """
    Get height: a and weight: b of rectangle and return his area
    ------------------------------------------------------------
    example of call: area(5, 4) = 20
    """
    return a * b


def perimeter(a, b):
    """
    Get height: a and weight: b of rectangle and return his perimeter
    -----------------------------------------------------------------
    example of call: perimeter(5, 4) = 18
    """
    return 2 * (a + b)
