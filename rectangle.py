def area(a, b):
    '''Аргументы:
    a - сторона a прямоугольника
    
    b - сторона b прямоугольника
    
    Возвращает: площадь прямоугольника
    
    Пример вызова: area(3,4)'''
    return a * b 

def perimeter(a, b): 
    '''Аргументы:
    a - сторона a прямоугольника
    
    b - сторона b прямоугольника
    
    Возвращает: периметр прямоугольника
    
    Пример вызова: perimeter(3,4)'''
    return (a + b)*2

