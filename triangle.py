def area(a, h):
	"""
	Get side of triangle and height lowered onto her and return area
	----------------------------------------------------------------
	example of call: area(2, 8) = 8
	"""
	return a * h / 2


def perimeter(a, b, c):
	"""
	Get sides of triangle and return his perimeter
	----------------------------------------------
	example of call: perimeter(2, 3, 4) = 9
	"""
	return a + b + c
