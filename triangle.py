
def area(a, h): 
    '''
    Функция принимает на вход числа a(сторона), h(высота) и возвращает площадь Треугольника

    Вход: 3 6 Выход: 9
    '''
    return a * h / 2 

def perimeter(a, b, d):
    '''
    Функция принимает на вход 3 числа - стороны треугольника: a, b, d и возвращает периметр Треугольника

    Вход: 5 10 5 Выход: 20
    '''
    return a + b + d 