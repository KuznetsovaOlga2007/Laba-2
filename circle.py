import math


def area(r):
    '''Аргументы:
    r - радиус круга

    Возвращает: площадь круга
    
    Пример вызова: area(5)'''
    return math.pi * r * r


def perimeter(r):
    '''Аргументы:
    r - радиус круга
    
    Возвращает: периметр круга
    
    Пример вызова: perimeter(5)'''
    return 2 * math.pi * r

