import math


def area(r):
    '''Принимает число r, возвращает число типа float - площадь круга - квадрат числа r, умноженный на число пи'''
    return math.pi * r * r


def perimeter(r):
    '''Принимает число г, возвращает число типа float - периметр круга - удвоенное произведение r и числа пи'''
    return 2 * math.pi * r

