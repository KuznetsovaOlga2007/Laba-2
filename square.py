def area(a):
    """
    Вычисляет площадь квадрата по длине его стороны.

    Аргументы:
    a -- длина стороны квадрата

    Возвращает:
    Площадь квадрата.

    Пример:
    >>> area(4)
    16
    """
    return a * a

