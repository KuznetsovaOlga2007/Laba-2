
def area(a):
    '''Принимает сторону квадрата a, возвращает площадь квадрата.
       Площадь квадрата находится по формуле a * a (нужно значение стороны квадрата возвести в квадрат).'''
    return a * a


def perimeter(a):
    '''Принимает сторону квадрата a, возвращает его периметр.
       Чтобы найти периметр квадрата, нужно его сторону умножить на четыре (4 * a).'''
    return 4 * a
