'''
Принимает значение a, возращает значение площади квадрата
    Параметры:
            a (int): десятичное число
    Пример:
            Получает: a=6
            Возвращает: 36
'''
def area(a):
    return a * a


'''
Принимает значение a, возращает значение периметра квадрата
    Параметры:
            a (int): десятичное число
    Пример:
            Получает: a=6
            Возвращает: 24
'''
def perimeter(a):
    return 4 * a
