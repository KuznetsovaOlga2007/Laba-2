def area(a):
    """
    area(a)
    
    Принимает значения a и возвращает значение площади
    
    Пример: area(5) = 25
    """
    return a * a


def perimeter(a):
    """ 
    perimeter(a)
    
    Принимает значения a и возвращает значение периметра

    Пример: perimeter(5) = 20
    """
    return 4 * a
