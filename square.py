def area(a):
    '''Принимает значение стороны квадрата a и возвращает площадь квадрата.'''
    if a > 0:
        return a * a
    else:
        return 0

def perimeter(a):
    '''Принимает значение стороны квадрата a и возвращает периметр квадрата.'''
    if a > 0:
        return 4 * a
    else:
        return 0

